package com.carol8.datsevenimente.model;

public class Noutate {
    private String nume, descriere;

    public Noutate(String nume, String descriere) {
        this.nume = nume;
        this.descriere = descriere;
    }

    public String getNume() {
        return nume;
    }

    public String getDescriere() {
        return descriere;
    }
}
